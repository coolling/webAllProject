package com.example.sduManage.entity;


public class Social {

  private long socailId;
  private String title;
  private String ashort;
  private String along;
  private String photo;
  private String userName;


  public long getSocailId() {
    return socailId;
  }

  public void setSocailId(long socailId) {
    this.socailId = socailId;
  }


  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }


  public String getAshort() {
    return ashort;
  }

  public void setAshort(String ashort) {
    this.ashort = ashort;
  }


  public String getAlong() {
    return along;
  }

  public void setAlong(String along) {
    this.along = along;
  }


  public String getPhoto() {
    return photo;
  }

  public void setPhoto(String photo) {
    this.photo = photo;
  }


  public String getUserName() {
    return userName;
  }

  public void setUserName(String userName) {
    this.userName = userName;
  }

}
